// Shared types for the anti-cheat town frontend

export type PlayerPhase = 'normal' | 'suspicious' | 'investigating' | 'confirmed' | 'punishing' | 'monitoring' | 'offline'

export type CheatType = 'fly' | 'speed' | 'kill_aura' | 'x_ray' | 'scaffold' | 'auto_clicker' | 'reach'

export type Confidence = 'low' | 'medium' | 'high'

export type Weather = 'clear' | 'rain' | 'thunder' | 'snow'

export type TimePeriod = 'dawn' | 'morning' | 'noon' | 'afternoon' | 'dusk' | 'night'

export interface Vec3 {
  x: number
  y: number
  z: number
}

export interface BuildingInfo {
  id: string
  name: string
  position: Vec3
  size: Vec3
  color: number
}

export interface MonitorStation {
  id: string
  position: Vec3
  occupied: boolean
  occupantId: string | null
  screenState: Record<string, unknown> | null
}

export interface NPCRole {
  id: string
  label: string
  color: number
}

