export const CONFIDENCE_ORDER: Confidence[] = ['low', 'medium', 'high']

export interface AlertEntry {
  id: string
  playerId: string
  playerName: string
  cheatType: CheatType
  confidence: Confidence
  message: string
  timestamp: number
}

export interface Evidence {
  metric: string
  value: number
  threshold: number
  duration: number
}

export interface PhaseTransition {
  from: PlayerPhase
  to: PlayerPhase
  timestamp: number
}

export interface BanStatus {
  isBanned: boolean
  reason?: string
  duration?: string
  bannedAt?: number
  expiresAt?: number | null
  source?: string
}

export interface PlayerInfo {
  playerId: string
  name: string
  ip: string
  gameMode: string
  phase: PlayerPhase
  position: Vec3
  speed: number
  cps: number
  hitRate: number
  cheatRecordCount: number
  stationId?: string
  evidence: Evidence[]
  phaseHistory: PhaseTransition[]
  isTracked: boolean
  // VP 积分
  totalVP: number
  vpByType: Record<CheatType, number>
  penaltyCount: number
  // 封禁状态
  banStatus: BanStatus
}

export interface RecordsArchiveFilter {
  cheatType?: CheatType
  confidence?: Confidence
  dateFrom?: number
  dateTo?: number
  sortBy?: string
  sortDir?: 'asc' | 'desc'
  page?: number
  pageSize?: number
}

export interface CheatRecordEntry {
  id: string
  cheatType: CheatType
  confidence: Confidence
  timestamp: number
  action: string
  playerName?: string
  evidence?: Array<{ metric: string; value: number; threshold: number; duration: number }>
  vp?: number
}

export interface ServerStats {
  onlinePlayers: number
  totalPlayers: number
  activeAlerts: number
  alertsByType: Record<CheatType, number>
  totalBans: number
  whitelistCount: number
  totalPenalties: number
  autoPenalties: number
}

export type PenaltyLevel = 'L0' | 'L1' | 'L2' | 'L3' | 'L4' | 'L5'

export interface PenaltyLogEntry {
  penaltyId: string
  playerId: string
  playerName: string
  level: PenaltyLevel
  action: string
  cheatType: CheatType
  confidence: Confidence
  vp: number
  reason: string
  autoGenerated: boolean
  timestamp: number
}

